    /**
     * Get user data by user id
     * @param int $id
     * @return JsonResponse
     */
    public function getByIdUser( int $id ): JsonResponse
    {
        $response = null;
        try{
            $response = response()->json( UserData::query()->where( 'id_user', $id )->firstOrFail()->toArray() );
        }
        catch ( \Exception $e ){
            $response = response()->json(['ok' => false, 'message' => $e->getMessage() ], 421 );
        }

        return $response;
    }
